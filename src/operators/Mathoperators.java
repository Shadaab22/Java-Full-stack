package operators;
public class Mathoperators {
  public static void main(String[] args) {
		int a = 10;
		int b = 13;
		System.out.println("a + b =" + (a+b));
		System.out.println("a - b =" + (a-b));
		System.out.println("a * b =" + (a*b));
		System.out.println("a / b =" + (a/b));
		System.out.println("a % b =" + (a%b));

	}
}